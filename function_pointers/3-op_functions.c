#include "3-calc.h"

/**
* op_add - add
* @a: int a
* @b: int b
* Return: result
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - add
* @a: int a
* @b: int b
* Return: result
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - add
* @a: int a
* @b: int b
* Return: result
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - add
* @a: int a
* @b: int b
* Return: result
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - add
* @a: int a
* @b: int b
* Return: result
*/
int op_mod(int a, int b)
{
	return (a % b);
}
